dpoint VertexInterp(int isolevel,dpoint p1,dpoint p2,int valp1,int valp2) //point where sphere is intersecting
{
	double mu;
	struct dpoint p;
	mu=(double)(isolevel-valp1)/(double)(valp2-valp1);
	//printf("mu=%f",mu);
	p.x=p1.x+ (mu * (p2.x - p1.x));
	p.y=p1.y+ (mu * (p2.y - p1.y));
	p.z=p1.z+ (mu * (p2.z - p1.z));
	//printf(" \npoint is x=%f y=%f z=%f",p.x,p.y,p.z);
	return (p);
	 
}
